        });
    };

    const appendPageNumber = (index) => {
        const pageNumber = document.createElement("button");
        pageNumber.className = "pagination-number";
        pageNumber.innerHTML = index;
        pageNumber.setAttribute("page-index", index);
        pageNumber.setAttribute("aria-label", "Page " + index);

        paginationNumbers.appendChild(pageNumber);
    };

    const getPaginationNumbers = () => {
        for (let i = 1; i <= pageCount; i++) {
            appendPageNumber(i);
        }
    };

    const setCurrentPage = (pageNum) => {
        currentPage = pageNum;

        handleActivePageNumber();
        handlePageButtonsStatus();

        const prevRange = (pageNum - 1) * paginationLimit;
        const currRange = pageNum * paginationLimit;

        listItems.forEach((item, index) => {
            item.classList.add("hidden__blogs");
            if (index >= prevRange && index < currRange) {
                item.classList.remove("hidden__blogs");
            }
        });
    };

    window.addEventListener("load", () => {
        getPaginationNumbers();
        setCurrentPage(1);

        prevButton.addEventListener("click", () => {
            setCurrentPage(currentPage - 1);
            scrollBlogs();
        });

        nextButton.addEventListener("click", () => {
            setCurrentPage(currentPage + 1);
            scrollBlogs();
        });

        document.querySelectorAll(".pagination-number").forEach((button) => {
            const pageIndex = Number(button.getAttribute("page-index"));

            if (pageIndex) {
                button.addEventListener("click", () => {
                    setCurrentPage(pageIndex);
                });
            }
        });
    });

    function scrollBlogs() {
        let e = document.getElementsByTagName("body")[0];
        e.scrollIntoView({
            //   block: 'start',
            behavior: 'smooth',
            //   inline: 'start'
        });
    }

        // tag filter
        $(".tags-list span,.cat-links a").click(function(e){
        e.preventDefault();
        var clickLink = e.target.innerText.toLowerCase();
        

        $(".new-blog_wrappper .post-item").each(function(){
            var tagPost = $(this).children('.entry-container').find('a').eq(0).text().toLowerCase()
            if(clickLink != tagPost){
                $(this).hide();
            }else{
                $(this).show();
            }
        })

    });
