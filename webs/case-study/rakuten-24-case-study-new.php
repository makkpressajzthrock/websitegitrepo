            <h3>Performance Measurement </h3>
            <p>To gain a more accurate understanding of user experience, Rakuten 24 determined to leverage the web-vitals JavaScript library to measure Core Web Vitals and other metrics in the field and transmit the data to their in-house analytics tool, in addition to using PageSpeed Insights for website auditing. </p>

            <h3>Performance Analyzing</h3>
            <p>The team examined the field data they gathered to see if there was any connection between Core Web Vitals and key business metrics. They discovered that those users who converted had a better LCP than those who didn't.</p>

            <h3>Performance Monitoring</h3>
            <p>The team constructed a dashboard for performance monitoring utilizing the data acquired in the field and a business intelligence tool. This is essential for tracking progress and avoiding any backsliding</p>

            <h3>A/B Test </h3>
            <p>The team conducted an A/B test to measure the business impact of their performance optimizations by optimizing one of their landing pages for Core Web Vitals. They chose a page with high traffic and conversion to ensure meaningful results. During the testing period, 50% of visitors were directed to the optimized page (version A), while the other 50% were sent to the original page (version B). The only difference between the two versions was that version A was optimized for Core Web Vitals, and there were no other changes.</p>

